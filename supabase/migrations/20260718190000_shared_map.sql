create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  display_name text,
  role text not null default 'participant' check (role in ('participant', 'moderator', 'admin')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.points (
  id text primary key,
  route_order integer,
  chapter text not null default 'railway',
  title text not null,
  short_title text,
  latitude double precision,
  longitude double precision,
  coordinate_status text not null default 'needs-check' check (coordinate_status in ('verified', 'needs-check')),
  status text not null default 'research' check (status in ('research', 'draft', 'published', 'archived')),
  content jsonb not null default '{}'::jsonb,
  created_by uuid references auth.users(id),
  updated_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.routes (
  id text primary key,
  title text not null,
  travel_mode text not null default 'walking' check (travel_mode in ('walking', 'manual')),
  status text not null default 'published' check (status in ('draft', 'published', 'archived')),
  updated_by uuid references auth.users(id),
  updated_at timestamptz not null default now()
);

insert into public.routes (id, title, travel_mode, status)
values ('main', 'Большой экскурсионный маршрут по Кеми', 'walking', 'published')
on conflict (id) do nothing;

create table if not exists public.route_waypoints (
  id uuid primary key default gen_random_uuid(),
  route_id text not null references public.routes(id) on delete cascade,
  position integer not null,
  point_id text references public.points(id) on delete set null,
  latitude double precision not null,
  longitude double precision not null,
  kind text not null default 'via' check (kind in ('stop', 'via')),
  created_by uuid references auth.users(id),
  updated_at timestamptz not null default now(),
  unique (route_id, position)
);

create table if not exists public.photos (
  id uuid primary key default gen_random_uuid(),
  point_id text not null references public.points(id) on delete cascade,
  title text not null,
  caption text not null default '',
  shot_date text,
  author text,
  source text,
  rights_status text,
  storage_path text not null unique,
  status text not null default 'draft' check (status in ('draft', 'published', 'archived')),
  created_by uuid not null references auth.users(id),
  updated_by uuid references auth.users(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.point_revisions (
  id bigint generated by default as identity primary key,
  point_id text not null,
  changed_by uuid references auth.users(id),
  previous_value jsonb,
  new_value jsonb,
  changed_at timestamptz not null default now()
);

create or replace function public.set_updated_at()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;


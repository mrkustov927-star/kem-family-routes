create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.profiles (id, display_name)
  values (new.id, coalesce(new.raw_user_meta_data ->> 'display_name', split_part(new.email, '@', 1)))
  on conflict (id) do nothing;
  return new;
end;
$$;

create or replace function public.current_user_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select role from public.profiles where id = auth.uid();
$$;

create or replace function public.protect_profile_role()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.role is distinct from old.role
    and auth.uid() is not null
    and public.current_user_role() <> 'admin' then
    raise exception 'Only an administrator can change participant roles';
  end if;
  return new;
end;
$$;

drop trigger if exists profiles_protect_role on public.profiles;
create trigger profiles_protect_role before update on public.profiles
for each row execute function public.protect_profile_role();

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute function public.handle_new_user();

create or replace function public.audit_point_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.point_revisions (point_id, changed_by, previous_value, new_value)
  values (new.id, auth.uid(), to_jsonb(old), to_jsonb(new));
  return new;
end;
$$;

drop trigger if exists points_audit_change on public.points;
create trigger points_audit_change
after update on public.points
for each row execute function public.audit_point_change();

create or replace function public.replace_main_route_waypoints(p_waypoints jsonb)
returns void
language plpgsql
security invoker
set search_path = public
as $$
begin
  if auth.uid() is null then
    raise exception 'Authentication required';
  end if;

  delete from public.route_waypoints where route_id = 'main';

  insert into public.route_waypoints (
    route_id, position, point_id, latitude, longitude, kind, created_by
  )
  select
    'main',
    ordinality::integer,
    nullif(item ->> 'pointId', ''),
    (item ->> 'lat')::double precision,
    (item ->> 'lng')::double precision,
    case when item ->> 'kind' = 'stop' then 'stop' else 'via' end,
    auth.uid()
  from jsonb_array_elements(p_waypoints) with ordinality as items(item, ordinality);

  update public.routes
  set updated_by = auth.uid(), updated_at = now()
  where id = 'main';
end;
$$;

